// Syntax : var variableName = value;

// String

var name = "Arun";

console.log(name);

// Redeclaration

var name = "Arun Kumar";

console.log(name);

// Updating

name = "Arun Kumar Balagani";

console.log(name);

// ---------------------------------------------------------------

// Number

var x = 10;

console.log(x);

// Redeclaration

var x = 20;

console.log(x);

// Updating

x = 30;

console.log(x);

// ---------------------------------------------------------

// Boolean

var Boolean = true;

console.log(Boolean);

// Redeclaration

var Boolean = false;

console.log(Boolean);


// Updating

var Boolean = true;

console.log(Boolean);

// ----------------------------------------------------------------

// Null

var accessRole = null;

console.log(accessRole);

// Redeclaration

var accessRole = 1;
console.log(accessRole);


// Updating

accessRole = 2;

console.log(accessRole);

// ----------------------------------------------------------------

// Undefined

var userName;

console.log(userName);

// Redeclaration

var userName = null;

console.log(userName);

// Updating

userName = "Arun";

console.log(userName);

// ---------------------------------------------------------------------

// Arrays

var fruits = [];

console.log(fruits);

// Redeclatation

var fruits = ["apple", "orange", "mango", "grapes", "dates"];

console.log(fruits);

// Updating

fruits = ["apple", "orange", "mango", "grapes", "dates", "pine apple"];

console.log(fruits);

